purse = dict()       #Creates an empty dictionary
purse['money'] = 100 #Creates a key 'money' with value 100 stored in it.
purse['candy'] = 10  
print(purse)          #  {'money': 100, 'candy': 10}
purse['candy'] += 10  # Adds 10 to the value of key 'candy'
print(purse)          # {'money': 100, 'candy': 20}